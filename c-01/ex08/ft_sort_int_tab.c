void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	temp;
	int	sorted;

	i = 0;
	j = 0;
	sorted = 0;
	while (!sorted)
	{
		sorted = 1;
		j = 0;
		while (j < size - 1 - i)
		{
			if (tab[j] > tab[j + 1])
			{
				temp = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = temp;
				sorted = 0;
			}
			j++;
		}	
	i++;
	}
}
